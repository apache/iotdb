package org.apache.iotdb.consensus.iot;

import org.apache.iotdb.consensus.i18n.IoTConsensusMessages;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;

import java.util.Collections;
import java.util.HashMap;
import java.util.Map;

/**
 * Tracks per-writer safe frontier on the receiving side.
 *
 * <p>Each writer keeps at most one pending safe physical time and its local-sequence barrier
 * because barriers generated by the same writer are expected to be totally ordered by both fields.
 */
public class WriterSafeFrontierTracker {

  private static final Logger LOGGER = LoggerFactory.getLogger(WriterSafeFrontierTracker.class);

  private final Map<Integer, WriterFrontierState> states = new HashMap<>();

  public synchronized void recordAppliedProgress(
      final long physicalTime, final int writerNodeId, final long appliedLocalSeq) {
    final WriterFrontierState state =
        states.computeIfAbsent(writerNodeId, ignored -> new WriterFrontierState());
    state.appliedLocalSeq = Math.max(state.appliedLocalSeq, appliedLocalSeq);
    if (physicalTime > 0) {
      state.effectiveSafePt = Math.max(state.effectiveSafePt, physicalTime);
    }
    promotePendingIfReady(state);
  }

  public synchronized void observePendingWriterSafeTimeBarrier(
      final long safePhysicalTime, final int writerNodeId, final long barrierLocalSeq) {
    if (safePhysicalTime <= 0) {
      return;
    }
    final WriterFrontierState state =
        states.computeIfAbsent(writerNodeId, ignored -> new WriterFrontierState());
    if (state.appliedLocalSeq >= barrierLocalSeq) {
      state.effectiveSafePt = Math.max(state.effectiveSafePt, safePhysicalTime);
      state.clearPendingSafeTime();
      return;
    }
    if (!state.hasPendingSafeTime()) {
      state.recordPendingSafeTime(safePhysicalTime, barrierLocalSeq);
      return;
    }
    if (dominates(
        safePhysicalTime,
        barrierLocalSeq,
        state.pendingSafePhysicalTime,
        state.pendingSafeLocalSeq)) {
      state.recordPendingSafeTime(safePhysicalTime, barrierLocalSeq);
      return;
    }
    if (dominates(
        state.pendingSafePhysicalTime,
        state.pendingSafeLocalSeq,
        safePhysicalTime,
        barrierLocalSeq)) {
      return;
    }
    LOGGER.warn(
        IoTConsensusMessages.LOG_OBSERVED_INCOMPARABLE_WRITER_SAFE_TIME_BARRIER_WRITER_ARG_0F0C171D
            + IoTConsensusMessages
                .LOG_KEEP_PENDINGSAFEPHYSICALTIME_ARG_PENDINGSAFELOCALSEQ_ARG_7123FD95
            + IoTConsensusMessages.LOG_IGNORE_SAFEPHYSICALTIME_ARG_SAFELOCALSEQ_ARG_A601C4D1,
        writerNodeId,
        state.pendingSafePhysicalTime,
        state.pendingSafeLocalSeq,
        safePhysicalTime,
        barrierLocalSeq);
  }

  public synchronized long getEffectiveSafePt(final int writerNodeId) {
    final WriterFrontierState state = states.get(writerNodeId);
    return state != null ? state.effectiveSafePt : 0L;
  }

  public synchronized Map<Integer, Long> snapshotEffectiveSafePts() {
    final Map<Integer, Long> snapshot = new HashMap<>();
    for (final Map.Entry<Integer, WriterFrontierState> entry : states.entrySet()) {
      snapshot.put(entry.getKey(), entry.getValue().effectiveSafePt);
    }
    return Collections.unmodifiableMap(snapshot);
  }

  private void promotePendingIfReady(final WriterFrontierState state) {
    if (!state.hasPendingSafeTime()) {
      return;
    }
    if (state.appliedLocalSeq >= state.pendingSafeLocalSeq) {
      state.effectiveSafePt = Math.max(state.effectiveSafePt, state.pendingSafePhysicalTime);
      state.clearPendingSafeTime();
    }
  }

  private static boolean dominates(
      final long leftSafePhysicalTime,
      final long leftSafeLocalSeq,
      final long rightSafePhysicalTime,
      final long rightSafeLocalSeq) {
    return leftSafePhysicalTime >= rightSafePhysicalTime && leftSafeLocalSeq >= rightSafeLocalSeq;
  }

  private static final class WriterFrontierState {
    private long appliedLocalSeq = 0L;
    private long effectiveSafePt = 0L;
    private long pendingSafePhysicalTime = 0L;
    private long pendingSafeLocalSeq = 0L;

    private boolean hasPendingSafeTime() {
      return pendingSafePhysicalTime > 0;
    }

    private void recordPendingSafeTime(final long safePhysicalTime, final long safeLocalSeq) {
      this.pendingSafePhysicalTime = safePhysicalTime;
      this.pendingSafeLocalSeq = safeLocalSeq;
    }

    private void clearPendingSafeTime() {
      pendingSafePhysicalTime = 0L;
      pendingSafeLocalSeq = 0L;
    }
  }
}
